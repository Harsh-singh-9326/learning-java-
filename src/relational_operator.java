public class relational_operator {
    public static void main(String[] args) {
        int a = 10 , b = 20;
        System.out.println(a + " " +b);
        // relation operator are used to check relation between operand
        System.out.println(a==b); // is equal to                 //false
        System.out.println(a!=b); // not equal to                //true
        System.out.println(a<=b); // is greater or equal to      //true
        System.out.println(a>=b); // is smaller or equal to      //false
        System.out.println(a>b);  // is greater                  //false
        System.out.println(a<b); // is smaller                   //true

    }
}
